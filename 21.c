/*
1.数位输出
问题描述：
输入一个5位整数，求出其各数位数值，并按照从高位到低位的顺序输出，如：输入12345，输出为1 2 3 4 5。

输入说明：
输入一个五位正整数。

输出说明：
按数位从高到低依次输出，各数位之间以一个空格相分隔。

输入样例：
96237

输出样例：
    9 6 2 3 7
*/

#include<stdio.h>
int main()
{
	int d5,a,b,c,d,e;
	scanf("%d",&d5);
	e=d5%10;
	d=((d5-e)/10)%10;
	a=d5/10000;
	c=((d5-e-d*10)/100)%10;
	b=(d5-a*10000)/1000;
	printf("%d %d %d %d %d",a,b,c,d,e);
	return 0;
 } 
