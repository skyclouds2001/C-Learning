/*
标题:
计算整数各位数字之和
类别:
时间限制:	
1s
内存限制:	
1000Kb
问题描述:	
假设n是一个由最多9位数字（d9, …, d1）组成的正整数。编写一个程序计算n的每一位数字之和
输入说明:	
输入数据为一个正整数n
输出说明:	
对整数n输出它的各位数字之和后换行
输入样例:	
3704
输出样例:	
14
*/

#include<stdio.h>
int main()
{
	int n,t;
	short d[9]={0},i,sum=0;
	scanf("%d",&n);
	t=n;
	for(i=0;i<9;i++)
	{
		d[i]=t%10;
		t/=10;
		sum+=d[i];
	}
	printf("%hd",sum);
	return 0;
}

