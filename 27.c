/*
题目：阶梯电价计费
类别：流程控制	
时间限制：2S
内存限制：10000Kb
问题描述：	
电价分三个档次，[0,110]度电，每度电0.5元；(110,210]度电，超出110部分每度电0.55元，超过210度电，超出210部分每度电0.70元，给出一个家庭一月用电量，请计算出应缴的电费（四舍五入，保留小数点后两位小数）。
输入说明：
输入数据为一个正实数，表示一月用电量
输出说明：
输出应缴电费，四舍五入保留2位小数。
输入样例：
输入样例1
100
输入样例2
200
输入样例3
329.75
输出样例：
输出样例1
50.00
输出样例2
104.50
输出样例3
193.82
*/

#include<stdio.h>
int main()
{
	float price,elec;
	scanf("%f",&elec);
	if(elec<=110&&elec>=0)
	{
		price=elec*0.5;
	}
	else if(elec>110&&elec<=210)
	{
		price=110*0.5+(elec-110)*0.55;
	}
	else if(elec>210)
	{
		price=110*0.5+(210-110)*0.55+(elec-210)*0.7;
	}
	printf("%.2f",price);
	return 0 ;
}
