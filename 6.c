/*
题目6：数字排序
问题描述
　　给定n个整数，请计算每个整数各位数字和，按各位数字和从大到小的顺序输出。
输入格式
　　输入的第一行包含一个整数n，表示给定数字的个数。
　　第二行包含n个整数，相邻的整数之间用一个空格分隔，表示所给定的整数。
输出格式
　　输出多行，每行包含两个整数，分别表示一个给定的整数和它的各位数字和。按各位数字和递减的顺序输出。
    如果两个整数各位数字和相同，则先输出值较小的，然后输出值较大的。
样例输入
5
101 100 999 1234 110
样例输出
999 27
1234 10
101 2
110 2
100 1
评测用例规模与约定
　　1 ≤ n ≤ 1000，给出的数都是不超过10000的非负整数。
*/

#include<stdio.h>
int main()
{
	int n;
	int a[1000]={0},b[1000]={0};
	int i,j;
	int p,q;
	scanf("%d",&n);
	for(i=0;i<n;i++)
	{
		scanf("%d",&a[i]);
	}
	printf("\n");
	for(i=0;i<n;i++)
	{
		p=a[i];
		q=0;
		if(p>=0&&p<=9) 
		{
			q=p;
		}
		else if(p>=10&&p<=99)
		{
			q=p%10+p/10;
		}
		else if(p>=100&&p<=999)
		{
			q=p%10+p%100/10+p/100;
		}
		else if(p>=1000&&p<=9999)
		{
			q=p%10+p%100/10+p%1000/100+p/1000;
		}
		b[i]=q;
	}
	for(i=0;i<n;i++)
	{
		for(j=n;j>i;j--)
		{
			if(b[j]>b[j-1])
			{
				p=b[j];
				b[j]=b[j-1];
				b[j-1]=p;
				q=a[j];
				a[j]=a[j-1];
				a[j-1]=q;
			}
			else if(b[j]==b[j-1])
			{
				if(a[j]<a[j-1])
				{
					p=b[j];
					b[j]=b[j-1];
					b[j-1]=p;
					q=a[j];
				    a[j]=a[j-1];
				    a[j-1]=q;
				}
			}
		}
	}
	for(i=0;i<n;i++)
	{
		printf("%d %d \n",a[i],b[i]);
	}
	return 0;
}
